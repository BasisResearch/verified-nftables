(** ** Concrete witness 1 — the spoofing attempt is blocked.

    In the three concrete corollaries the [e = gen_env] pin is a WITNESSING
    instantiation, not a hypothesis to relax: it supplies the parser's own set
    contents so the memberships compute, and — unlike the pre-M4 general form
    or Optiplex_Mark — no env-reading field hypothesis coexists with it, so
    the hypotheses are jointly satisfiable (the frames below are real).

    The container behind `inc-vikun` (vikunja, real address .14) sends a frame to
    budget's address 192.168.51.20.  The pair (192.168.51.20 . inc-vikun) is NOT
    in vmantispoof, so the frame is dropped: vikunja cannot impersonate budget. *)
Theorem vikunja_cannot_spoof_budget : forall e p,
  e = gen_env ->
  field_value Fobrname e p = ifreg "br.20" ->
  field_value FMetaProtocol e p = [8; 0] ->              (* an IPv4 frame *)
  read_payload_ok PNetwork 16 4 p = true ->            (* a well-formed IPv4 header *)
  field_value FIp4Daddr e p = ip4 192 168 51 20 ->      (* budget's address *)
  field_value FMetaOifname e p = ifreg "inc-vikun" ->   (* vikunja's interface *)
  eval_table vm_fuel vmfilter_chains vmfilter_output e p = Drop.
Proof.
  intros e p Henv Hobr Hethip Hok Hdaddr Hoif; subst e.
  apply antispoof_general; auto.
  - rewrite Hdaddr. vm_compute. reflexivity.
  - rewrite Hdaddr, Hoif. vm_compute. reflexivity.
Qed.

(** A second spoof: gentoo (behind vb-gentoo) trying to take hass's .10. *)
Theorem gentoo_cannot_spoof_hass : forall e p,
  e = gen_env ->
  field_value Fobrname e p = ifreg "br.20" ->
  field_value FMetaProtocol e p = [8; 0] ->              (* an IPv4 frame *)
  read_payload_ok PNetwork 16 4 p = true ->
  field_value FIp4Daddr e p = ip4 192 168 51 10 ->       (* hass's address *)
  field_value FMetaOifname e p = ifreg "vb-gentoo" ->    (* gentoo's interface *)
  eval_table vm_fuel vmfilter_chains vmfilter_output e p = Drop.
Proof.
  intros e p Henv Hobr Hethip Hok Hdaddr Hoif; subst e.
  apply antispoof_general; auto.
  - rewrite Hdaddr. vm_compute. reflexivity.
  - rewrite Hdaddr, Hoif. vm_compute. reflexivity.
Qed.

(** ** Concrete witness 2 — legitimate traffic is allowed.

    Budget's own frame (to .20, leaving its bound interface inc-budge) is NOT
    dropped: the (.20 . inc-budge) pair IS in vmantispoof, so the negated match
    fails, the rule does not fire, and the frame falls through to `policy accept`.
    The rule blocks *only* spoofing, not the legitimate binding. *)
Theorem budget_legitimate_allowed : forall e p,
  e = gen_env ->
  field_value Fobrname e p = ifreg "br.20" ->
  field_value FMetaProtocol e p = [8; 0] ->              (* an IPv4 frame *)
  read_payload_ok PNetwork 16 4 p = true ->
  field_value FIp4Daddr e p = ip4 192 168 51 20 ->
  field_value FMetaOifname e p = ifreg "inc-budge" ->    (* its OWN interface *)
  eval_table vm_fuel vmfilter_chains vmfilter_output e p = Accept.
Proof.
  intros e p Henv Hobr Hethip Hok Hdaddr Hoif. unfold Fobrname in Hobr.
  unfold eval_table, vm_fuel, vmfilter_output. cbn [c_rules c_policy].
  (* antispoof rule does not apply: the pair IS bound, so the `!=` match is false *)
  erewrite erj_skip.
  2:{ unfold rule_applies, rule_applies_walk, eval_matchcond, match_loadable, eval_matchcond_body,
        fields_loadable, field_loadable, load_ok.
      cbn -[field_value read_payload_ok].
      rewrite ?Hok, Hobr, Hethip, ?app_nil_r, Hdaddr, Hoif, ?Henv. vm_compute. reflexivity. }
  (* hass rule does not apply either: obrname is br.20, not br.1 *)
  erewrite erj_skip.
  2:{ unfold rule_applies, rule_applies_walk, eval_matchcond, match_loadable, eval_matchcond_body,
        fields_loadable, field_loadable, load_ok.
      cbn -[field_value read_payload_ok]. rewrite ?Hok, Hobr.
      vm_compute. reflexivity. }
  reflexivity.
Qed.

